# If Conditional Logic
if true
    puts "this is true"
end

if false
    puts "you should not ever see this"
end

# If/Else Conditional Logic
# Elsif Conditional Logic
temp = 65
threshold = 20

if temp >= threshold && threshold == 20
    puts "this is great"
elsif temp >= threshold && threshold == 10
    puts "OK!"
else
    puts "This is too cold"
end

